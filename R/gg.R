uglymerge <- function(xl) {
    out <- xl[[1]]
    for (ii in seq_along(xl)[-1]) {
        this <- xl[[ii]]
        nms <- setdiff(names(this), c("plot_sequence"))
        for (nn in nms) out[[nn]] <- this[[nn]]
        out$plot_sequence <- c(setdiff(out$plot_sequence, this$plot_sequence), this$plot_sequence)
    }
    out
}

